// Format date and time from ISO string
function formatDateTime(dateString: string): string {
  const date = new Date(dateString);
  return date.toLocaleString("en-US", {
    year: "numeric",
    month: "short",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  });
}

// Return icon based on alert type
function getAlertIcon(type: string) {
  switch (type) {
    case "info":
      return <InfoIcon className="size-4 text-blue-500" />;
    case "warning":
      return <AlertTriangleIcon className="size-4 text-amber-500" />;
    case "error":
      return <AlertCircleIcon className="size-4 text-red-500" />;
    case "success":
      return <CheckCircleIcon className="size-4 text-green-500" />;
    default:
      return <InfoIcon className="size-4" />;
  }
}

// Create a separate component for the drag handle
function DragHandle({ id }: { id: string }) {
  const { attributes, listeners } = useSortable({
    id,
  });

  return (
    <Button
      {...attributes}
      {...listeners}
      variant="ghost"
      size="icon"
      className="size-7 text-muted-foreground hover:bg-transparent"
    >
      <GripVerticalIcon className="size-3 text-muted-foreground" />
      <span className="sr-only">Drag to reorder</span>
    </Button>
  );
}

